def simple_chat(prompt):
    return groq_llm.invoke([HumanMessage(content=prompt)]).content

@app.route("/api/ask", methods=["POST"])
@login_required
def handle_ask():
    user_prompt = request.json.get("prompt")
    user_id = current_user.auth0_id
    
    # Routing
    try:
        router_system = "Classify: 1. 'simple' (jokes, greetings). 2. 'complex' (search, code, specific entities). Return ONLY 'simple' or 'complex'."
        classification = groq_llm.invoke([
            SystemMessage(content=router_system), 
            HumanMessage(content=user_prompt)
        ]).content.strip().lower()
    except:
        classification = 'complex' 

    final_answer = ""
    cost = 0.5

    if "simple" in classification:
        final_answer = simple_chat(user_prompt)
    else:
        config = {"configurable": {"thread_id": user_id}, "recursion_limit": 50}
        agent_sys = SystemMessage(content="You are a helpful AI. RULES: 1. Use 'web_search_tool' for people/news. 2. Don't guess.")
        
        try:
            # NOTE: We do NOT call setup() here anymore. It's done at startup.
            result = agent_app.invoke(
                {"messages": [agent_sys, HumanMessage(content=user_prompt)]}, 
                config=config
            )
            final_answer = result["messages"][-1].content
        except Exception as e:
            print(f"‚ö†Ô∏è Agent Error: {e}")
            final_answer = "I encountered a temporary error. Please try again."

    # Billing
    try:
        current_user.credits -= Decimal(cost)
        db.session.commit()
    except:
        db.session.rollback()

    return jsonify({
        "answer": final_answer,
        "cost": cost,
        "remaining": float(current_user.credits) if current_user.credits else 0.0
    })

