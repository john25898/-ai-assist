def simple_chat(prompt):
    return groq_llm.invoke([HumanMessage(content=prompt)]).content

@app.route("/api/ask", methods=["POST"])
@login_required
def handle_ask():
    user_prompt = request.json.get("prompt")
    user_id = current_user.auth0_id
    
    # --- ROUTING ---
    router_system = """
    Classify this prompt.
    1. 'simple': Greetings, jokes, definitions.
    2. 'complex': Requests for code, weather, news, specific people/places, or current events.
    
    IMPORTANT: If the user asks "Who is...", "What is the current...", or about a specific university/company, classify as 'complex'.
    Return ONLY 'simple' or 'complex'.
    """
    try:
        classification = groq_llm.invoke([
            SystemMessage(content=router_system), 
            HumanMessage(content=user_prompt)
        ]).content.strip().lower()
    except:
        classification = 'complex' 

    final_answer = ""
    cost_estimate = 0.0

    # --- EXECUTION ---
    if "simple" in classification:
        final_answer = simple_chat(user_prompt)
    else:
        config = {"configurable": {"thread_id": user_id}}
        config["recursion_limit"] = 50
        
        agent_system_message = SystemMessage(content="""
        You are a helpful AI assistant.
        RULES:
        1. If asked about specific people, leaders (VC, CEO), or current events, YOU MUST USE 'web_search_tool'.
        2. Do not say "I don't have up-to-date info". SEARCH FOR IT.
        3. If asked to write code, use the 'ui_builder_tool' or 'backend_builder_tool'.
        """)

        result = agent_app.invoke(
            {"messages": [agent_system_message, HumanMessage(content=user_prompt)]}, 
            config=config
        )
        final_answer = result["messages"][-1].content

    # --- BILLING ---
    current_user.credits -= Decimal(cost_estimate)
    db.session.commit()

    return jsonify({
        "answer": final_answer,
        "cost": cost_estimate,
        "remaining": float(current_user.credits)
    })

